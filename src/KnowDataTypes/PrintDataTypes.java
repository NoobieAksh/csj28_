package KnowDataTypes;
//Write a Java program to print an int, a double and a char on screen.

public class PrintDataTypes {
    public static void main(String[] args) {
        int number1 = 45;
        double number2 = 23.56d;
        char character = 'C';
        System.out.println("Integer Value: "+number1);
        System.out.println("Double Values: "+number2);
        System.out.println("Character: "+character);
    }
}
